--3.Employee_Attendance: --- select * from dbo.Employee_Attendance

DECLARE @StartDate DATE = '2024-01-01'
DECLARE @EndDate DATE = '2024-03-31'
DECLARE @EmployeeCount INT = 1
SET NOCOUNT ON

WHILE @EmployeeCount <= 100
BEGIN
    DECLARE @CurrentDate DATE = @StartDate
  
    WHILE @CurrentDate <= @EndDate
    BEGIN
        --  each day of the month
        WHILE @CurrentDate <= EOMONTH(@CurrentDate)
        BEGIN
            
            DECLARE @StartTime TIME = DATEADD(MINUTE, ROUND(RAND() * 150, 0), '08:00') -- 8:00 AM and 10:30 AM
            DECLARE @EndTime TIME = DATEADD(MINUTE, ROUND(RAND() * 120, 0), '18:00') -- 18:00 PM and 20:00 PM   
			DECLARE @IsHoliday BIT = CASE WHEN DATENAME(WEEKDAY, @CurrentDate) IN ('Saturday', 'Sunday') THEN 1 ELSE 0 END
            DECLARE @Hours_Worked DECIMAL(5, 2) = CASE WHEN @IsHoliday = 1 THEN 0.00 ELSE DATEDIFF(MINUTE, @StartTime, @EndTime) / 60.0 END
            DECLARE @IsLate BIT = CASE WHEN @StartTime > '10:00' THEN 1 ELSE 0 END

			DECLARE @IsPermission BIT = CASE WHEN @EndTime < '19:00' OR  @Hours_Worked < 10 THEN 1 ELSE 0 END

			
            DECLARE @EmployeeID INT = @EmployeeCount
            
            INSERT INTO dbo.Employee_Attendance (AttendanceDate, StartTime, EndTime, Hours_Worked, IsLate, IsHoliday, Employee_Id)
            VALUES (@CurrentDate, @StartTime, @EndTime, @Hours_Worked, @IsLate, @IsHoliday, @EmployeeID)

            SET @CurrentDate = DATEADD(DAY, 1, @CurrentDate)

            
            IF @CurrentDate > @EndDate
                BREAK
        END

        SET @CurrentDate = DATEADD(MONTH, 1, @CurrentDate)
    END

    SET @EmployeeCount = @EmployeeCount + 1
END


