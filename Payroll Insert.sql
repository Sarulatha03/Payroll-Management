    DECLARE @EmployeeID INT = @EmployeeCount
    
    DECLARE @MonthStartDate DATE = '2024-01-01'
    DECLARE @MonthEndDate DATE = '2024-03-31'
    DECLARE @CurrentMonth DATE = @MonthStartDate

    WHILE @CurrentMonth <= @MonthEndDate
    BEGIN
        DECLARE @Basic DECIMAL(10, 2)
			SELECT @Basic = Salary
			FROM dbo.Employee_Salary
			WHERE Employee_Id = @EmployeeID

        DECLARE @HRA DECIMAL(10, 2)
        DECLARE @DA DECIMAL(10, 2)
        DECLARE @OtherAllowances DECIMAL(10, 2)
        DECLARE @PF DECIMAL(10, 2)

			SELECT @HRA = av.HRA, @DA = av.DA, @OtherAllowances = av.OtherAllowances, @PF = av.PF
			FROM @Allowance_Values av
			WHERE av.Employee_Id = @EmployeeID

        DECLARE @LOPAmount DECIMAL(5, 2)
			SELECT @LOPAmount = ISNULL(LOP, 0)
			FROM dbo.LOP_Details
			WHERE Employee_Id = @EmployeeID AND Month = @CurrentMonth;
		
        INSERT INTO dbo.Payroll (Employee_ID, MonthYear,[Basic], HRA, DA, OtherAllowances, TotalEarnings, PF, TotalSalary, LOPAmount, Deductions, NetSalary)
        VALUES (
            @EmployeeID, -- Employee_ID
            @CurrentMonth, 
            @Basic,
            @HRA,
            @DA,
            @OtherAllowances,
            @Basic + @HRA + @DA + @OtherAllowances, -- TotalEarnings
            @PF,
            @Basic + @HRA + @DA + @OtherAllowances - @PF, -- TotalSalary
            @LOPAmount, -- LOPAmount
            @PF + @LOPAmount, -- Deductions
            (@Basic + @HRA + @DA + @OtherAllowances) - (@PF + @LOPAmount) -- NetSalary
        )

        SET @CurrentMonth = DATEADD(MONTH, 1, @CurrentMonth);
    END

